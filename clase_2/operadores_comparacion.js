//igualdad debil ==
console.log (1==1);
console.log(0==""); //da true porque convierte el string vacio a 0


//igualdad estricta ===
console.log(1==="1"); //da false porque no son del mismo tipo

//desigualdad debil !=
console.log(1!=2); //true
console.log(1!="1"); //da false porque convierte el string a numero
console.log(undefined != null); //da false porque los considera iguales

//desigualdad estricta !==
console.log(1!== "1"); //true porque no son del mismo tipo

//Mayor que >
console.log(5>3); //true

//Menor que <
console.log(3<5, "3<5"); //true

//Mayor o igual que >=
console.log(5>=5); //true   

